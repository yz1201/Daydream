CREATE DATABASE emp_Sys;
USE emp_Sys;

CREATE TABLE DEPT ( deptno INT PRIMARY KEY auto_increment COMMENT '部门编号，主键', dname VARCHAR ( 14 ) COMMENT '部门名称', loc VARCHAR ( 23 ) COMMENT '部门位置' );


CREATE TABLE EMP(
empno INT PRIMARY KEY auto_increment COMMENT '员工编号，主键',
ename VARCHAR(20) COMMENT '员工姓名',
job VARCHAR(20) COMMENT '工作',
mgr INT COMMENT '领导的员工编号',
hiredate date COMMENT '入职日期',
sal DOUBLE(7,2) COMMENT '工资',
comm DOUBLE(7,2) COMMENT '奖金',
deptno INT COMMENT '部门编号，外键',
CONSTRAINT fk_dept_emp FOREIGN KEY(deptno) REFERENCES dept(deptno)
);

CREATE TABLE SALGRADE(
grade DOUBLE(7,2) COMMENT '工资级别',
losal DOUBLE(7,2) COMMENT '最低值',
hisal DOUBLE(7,2) COMMENT '最高值'
);


